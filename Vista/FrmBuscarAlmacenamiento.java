    public void setEnts(List<Almacenamiento> ents) {
        this.ents = ents;
        if (ents == null || tableModel == null) {
            return;
        }
        tableModel.setNumRows(0);

        ents.forEach(almacenamiento -> tableModel.addRow(
                new Object[]{
                   almacenamiento.getId(),
                   almacenamiento.getProduccion(),
                   almacenamiento.getCantidad(),
                   almacenamiento.getFechaIngreso(),
                   almacenamiento.getFechaEgreso()
                    
                }
        ));
    }
    
