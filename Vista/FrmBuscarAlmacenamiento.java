    private void txtfiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtfiltroActionPerformed
        String searchText = txtfiltro.getText();
        if (searchText.trim().isEmpty()) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText));
        }
    }//GEN-LAST:event_txtfiltroActionPerformed

    private void txtfiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfiltroKeyReleased
        String searchText=txtfiltro.getText();
        if (searchText.trim().isEmpty()) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText));
        }
    }//GEN-LAST:event_txtfiltroKeyReleased

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        frm.EditarMini(false);
        frm.fechaEgresoEditable();
        int selectedRow = tabla.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        String id = tabla.getValueAt(selectedRow, 0).toString();
        Almacenamiento seleccionada = ents.stream()
        .filter(almacenamiento -> String.valueOf(almacenamiento.getId()).equals(id)) 
        .findFirst()
        .orElse(null);
        observer.show(seleccionada);
        this.dispose();
    }//GEN-LAST:event_btnSeleccionarActionPerformed
   
    

